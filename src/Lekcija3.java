public class Lekcija3 {
    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        System.out.println("Sabiranje: " + (a + b));
        System.out.println("Oduzimanje: " + (a - b));
        System.out.println("Mnozenje: " + (a * b));
        System.out.println("Dijeljenje: " + (a / b));     // integer dijeljenje
        System.out.println("Modulus (ostatak): " + (a % b));
    }
}